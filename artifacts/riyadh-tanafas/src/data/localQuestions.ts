// Compute category counts
LOCAL_CATEGORIES.forEach((cat) => {
  cat.count = LOCAL_QUESTIONS.filter((q) => q.category === cat.name).length;
});

export function getLocalCategories() {
  return LOCAL_CATEGORIES.filter((c) => c.count > 0);
}

export function getLocalTopics(category: string) {
  const topics = new Map<string, number>();
  LOCAL_QUESTIONS.filter((q) => q.category === category).forEach((q) => {
    topics.set(q.topic, (topics.get(q.topic) ?? 0) + 1);
  });
  return Array.from(topics.entries()).map(([name, count]) => ({ name, count }));
}

